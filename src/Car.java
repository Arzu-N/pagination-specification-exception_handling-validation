package com.sample;


public class Car {

  private long id;
  private String marka;
  private String model;
  private String fiyat;
  private long yash;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getMarka() {
    return marka;
  }

  public void setMarka(String marka) {
    this.marka = marka;
  }


  public String getModel() {
    return model;
  }

  public void setModel(String model) {
    this.model = model;
  }


  public String getFiyat() {
    return fiyat;
  }

  public void setFiyat(String fiyat) {
    this.fiyat = fiyat;
  }


  public long getYash() {
    return yash;
  }

  public void setYash(long yash) {
    this.yash = yash;
  }

}
